const generateReport = (input) => {
    let report = '';
    switch (input) {
        case 'View all departments':
            report = ''
            break;

        case 'View all roles':
            report = ''
            break;

        case 'View all employees':
            report = ''
            break;

        case 'Add a department':
            report = ''
            break;

        case 'Add a role':
            report = ''
            break;

        case 'Add an employee':
            report = ''
            break;

        case 'Update an employee role':
            report = ''
            break;
    }
}

module.exports = {
    generateReport,
};